public class switch1 {
    public static void main(String[] args) {
        String command="lala";
        switch(command){
            case "baab":
                System.out.println("baab");
                break;
            case "lala":
                System.out.println("lala");
                break;
            case "erzi":
                System.out.println("erzi");
                break;
            default:
                System.out.println("command error");
                break;
        }
    }
}
